def test_single_volume(image, img_t1, label, net, classes, patch_size=[256, 256],
                    test_save_path=None, case=None, z_spacing=7.0, val_or_test=False):
    image, img_t1, label = image.squeeze(0).cpu().detach().numpy(), img_t1.squeeze(0).cpu().detach().numpy(), label.squeeze(0).cpu().detach().numpy()
    # image, label = image.squeeze(0).cpu().detach().numpy(), label.squeeze(0).cpu().detach().numpy()

    if len(image.shape) == 3:
        prediction = np.zeros_like(label)
        for ind in range(image.shape[0]):
            slice = image[ind, :, :]
            slice_t1 = img_t1[ind, :, :]
            x, y = slice.shape[0], slice.shape[1]
            if x != patch_size[0] or y != patch_size[1]:
                slice = zoom(slice, (patch_size[0] / x, patch_size[1] / y), order=3)  # previous using 0
                slice_t1 = zoom(slice_t1, (patch_size[0] / x, patch_size[1] / y), order=3)  # 同样调整input_t1的大小
            input = torch.from_numpy(slice).unsqueeze(0).unsqueeze(0).float().cuda()
            input_t1 = torch.from_numpy(slice_t1).unsqueeze(0).unsqueeze(0).float().cuda()
            net.eval()
            with torch.no_grad():
                outputs = net(input, input_t1)[0]
                out = torch.argmax(torch.softmax(outputs, dim=1), dim=1).squeeze(0)
                out = out.cpu().detach().numpy()

                if x != patch_size[0] or y != patch_size[1]:
                    pred = zoom(out, (x / patch_size[0], y / patch_size[1]), order=0)
                else:
                    pred = out

                prediction[ind] = pred

    else:
        input = torch.from_numpy(image).unsqueeze(
            0).unsqueeze(0).float().cuda()
        net.eval()
        with torch.no_grad():
            out = torch.argmax(torch.softmax(net(input), dim=1), dim=1).squeeze(0)
            prediction = out.cpu().detach().numpy()

    metric_list = []

    for i in range(1, classes):

        metric_list.append(calculate_metric_percase(prediction == i, label == i))

    if test_save_path is not None and val_or_test is True:
        img_itk = sitk.GetImageFromArray(image.astype(np.float32))
        prd_itk = sitk.GetImageFromArray(prediction.astype(np.float32))

        # datasets_name = setting_config.datasets_name
        project_root = os.path.abspath(os.path.dirname(__file__))
        txt_folder = os.path.join(project_root, "data", "spacing_img")

        txt_name = f"{case}.txt"
        matching_txt_file = os.path.join(txt_folder, txt_name)
        with open(matching_txt_file, 'r') as f:
            lines = f.readlines()
            spacing = tuple(map(float, lines[0].strip().split(": ")[1][1:-1].split(", ")))

        img_itk.SetSpacing(spacing)
        prd_itk.SetSpacing(spacing)

        sitk.WriteImage(img_itk, test_save_path + '/' + case + "_img.nii.gz")
        sitk.WriteImage(prd_itk, test_save_path + '/' + case + "_pred.nii.gz")

    return metric_list, prediction, label